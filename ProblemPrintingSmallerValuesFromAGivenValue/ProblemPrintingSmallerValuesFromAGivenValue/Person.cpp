#include "Person.h"

//----------------------------------------------------------------------------------------------
Person::Person(int id, string name) {
	SetId(id);
	SetName(name);
}
//----------------------------------------------------------------------------------------------
void Person::SetId(int id) {
	this->id = id;
}
//----------------------------------------------------------------------------------------------

void Person::SetName(string name) {
	this->name = name;
}
//----------------------------------------------------------------------------------------------
int Person::GetId() {
	return this->id;
}
//----------------------------------------------------------------------------------------------
string Person::GetName() {
	return this->name;
}
//----------------------------------------------------------------------------------------------
